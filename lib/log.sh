warn()    { echo -e "  ${YELLOW}вҡ ${RESET}  ${YELLOW}$*${RESET}"; }
error()   { echo -e "\n  ${RED}вңҳ  ERROR:${RESET} $*\n" >&2; exit 1; }

# в”Җв”Җ Install status (overwrite current line) в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
status_installing() {
    printf "  ${YELLOW}в—Қ${RESET}  %-45s" "$* ..."
}

status_done() {
    printf "\r  ${GREEN}вң”${RESET}  %-45s  ${DIM}done${RESET}\n" "$*"
}

status_skipped() {
    echo -e "  ${DIM}вҖ“  $*${RESET}"
}

status_fail() {
    printf "\r  ${RED}вңҳ${RESET}  %-45s  ${RED}failed${RESET}\n" "$*"
}

# в”Җв”Җ Spinner в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
spinner() {
    local pid=$1
    local delay=0.08
    local i=0
    local frames=("в Ӣ" "в ҷ" "в №" "в ё" "в ј" "в ҙ" "в Ұ" "в §" "в Ү" "в Ҹ")
    while kill -0 "$pid" 2>/dev/null; do
        printf " ${CYAN}%s${RESET}" "${frames[$i]}"
        sleep "$delay"
        printf "\b\b"
        i=$(( (i + 1) % ${#frames[@]} ))
    done
    printf "  \b\b"
}

# в”Җв”Җ Run command silently with spinner; fail loudly в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
run_quiet() {
    local label="$1"; shift
    local tmp_err
    tmp_err="$(mktemp)"

    status_installing "$label"
    ("$@" >/dev/null 2>"$tmp_err") &
    local pid=$!
    spinner $pid
    wait $pid
    local code=$?

    if [[ $code -ne 0 ]]; then
        status_fail "$label"
        echo -e "\n  ${RED}Command:${RESET} $*"
        echo -e "  ${RED}Output:${RESET}"
        sed 's/^/    /' "$tmp_err" >&2
        rm -f "$tmp_err"
        error "Aborting due to above failure."
    fi

    rm -f "$tmp_err"
    status_done "$label"
}

# в”Җв”Җ Prompt helpers в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ

# prompt_input <VAR> <label> <default>
# Prints a styled prompt and stores result in VAR.
# If label contains "password", input is masked.
# Uses printf -v to avoid nameref issues under set -euo pipefail.
prompt_input() {
    local _var="$1"
    local label="$2"
    local default="$3"
    local _val=""

    local hint=""
    [[ -n "$default" ]] && hint="${DIM} (default: ${default})${RESET}"

    printf "  ${CYAN}?${RESET}  ${BOLD}%-22s${RESET}${hint}  " "$label"

    # Mask input for password fields
    if [[ "${label,,}" == *"password"* ]]; then
        read -rs _val || true
        echo ""
    else
        read -r _val || true
    fi

    # Fall back to default if empty
    [[ -z "$_val" ]] && _val="$default"

    # Assign to caller's variable
    printf -v "$_var" '%s' "$_val"
}

# confirm <message>  вҖ” returns 0 for yes, 1 for no
confirm() {
    printf "  ${CYAN}?${RESET}  ${BOLD}$*${RESET}  ${DIM}[Y/n]${RESET}  "
    local _ans=""
    read -r _ans || true
    [[ "${_ans,,}" != "n" ]] || return 1
    return 0
}

